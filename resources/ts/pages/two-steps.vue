<script setup lang="ts">
import { h } from 'vue'
import { useDisplay } from 'vuetify'
import authV1BottomShape from '@images/svg/auth-v1-bottom-shape.svg?raw'
import authV1TopShape from '@images/svg/auth-v1-top-shape.svg?raw'
import { VNodeRenderer } from '@layouts/components/VNodeRenderer'
import { themeConfig } from '@themeConfig'
import { useAbility } from '@/plugins/casl/useAbility'
import { authApi } from '@/services/api'

const { smAndUp } = useDisplay()

const router = useRouter()
const otp = ref('')
const isOtpInserted = ref(false)
const infoMessage = ref<string | null>(null)
const userData = useCookie('userData')
const { updateAbility } = useAbility()

const onFinish = async () => {
  isOtpInserted.value = true
  infoMessage.value = 'Verifying code...'

  try {
    // Simulate API call for OTP verification
    await new Promise(resolve => setTimeout(resolve, 800))

    // Fetch latest user data and abilities after successful verification
    const loginResp = await authApi.login({
      email: userData.value?.email || '',
      password: 'password', // This is a placeholder, replace with a secure way to re-authenticate
    })

    if (loginResp.success && loginResp.data) {
      useCookie<string>('accessToken').value = loginResp.data.access_token
      useCookie<any>('userData').value = {
        id: loginResp.data.user.id,
        fullName: loginResp.data.user.name,
        username: loginResp.data.user.email,
        avatar: loginResp.data.user.avatar,
        email: loginResp.data.user.email,
        role: loginResp.data.user.role,
      }
      // Note: Ability rules need to be handled separately or set to empty
      useCookie<any[]>('userAbilityRules').value = []
      updateAbility([])
    }

    infoMessage.value = 'Code verified successfully. Redirecting...'

    await new Promise(resolve => setTimeout(resolve, 1200))

    infoMessage.value = null

    // Redirect directly to role-specific dashboard
    const userRole = loginResp.success && loginResp.data ? loginResp.data.user.role : 'franchisor'
    switch (userRole) {
      case 'admin':
        router.push('/admin/dashboard')
        break
      case 'franchisor':
        router.push('/franchisor')
        break
      case 'franchisee':
        router.push('/franchisee/dashboard/sales')
        break
      case 'sales':
        router.push('/sales/lead-management')
        break
      default:
        router.push('/')
    }
  }
  catch (error) {
    infoMessage.value = 'Verification failed. Please try again.'
    isOtpInserted.value = false
  }
}
</script>

<template>
  <div class="auth-wrapper d-flex align-center justify-center pa-4">
    <div class="position-relative my-sm-16">
      <!-- ðŸ‘‰ Top shape -->
      <VNodeRenderer
        :nodes="h('div', { innerHTML: authV1TopShape })"
        class="text-primary auth-v1-top-shape d-none d-sm-block"
      />

      <!-- ðŸ‘‰ Bottom shape -->
      <VNodeRenderer
        :nodes="h('div', { innerHTML: authV1BottomShape })"
        class="text-primary auth-v1-bottom-shape d-none d-sm-block"
      />

      <!-- ðŸ‘‰ Auth card -->
      <VCard
        class="auth-card"
        max-width="600"
        :class="smAndUp ? 'pa-6' : 'pa-0'"
      >
        <VCardItem class="justify-center">
          <VCardTitle>
            <RouterLink to="/">
              <div class="app-logo">
                <VNodeRenderer :nodes="themeConfig.app.logo" />
                <h1 class="app-logo-title">
                  {{ themeConfig.app.title }}
                </h1>
              </div>
            </RouterLink>
          </VCardTitle>
        </VCardItem>

        <VCardText>
          <h4 class="text-h4 mb-1">
            Two Step Verification ðŸ’¬
          </h4>
          <p class="mb-1">
            We sent a verification code to your mobile. Enter the code from the mobile in the field below.
          </p>
          <h6 class="text-h6">
            ******1234
          </h6>
        </VCardText>

        <VCardText>
          <VForm @submit.prevent="() => {}">
            <VRow>
              <VCol cols="12">
                <VAlert
                  v-if="infoMessage"
                  type="info"
                  variant="tonal"
                  class="mb-4"
                >
                  {{ infoMessage }}
                </VAlert>
              </VCol>
              <!-- email -->
              <VCol cols="12">
                <h6 class="text-body-1">
                  Type your 6 digit security code
                </h6>
                <VOtpInput
                  v-model="otp"
                  :disabled="isOtpInserted"
                  type="number"
                  class="pa-0"
                  @finish="onFinish"
                />
              </VCol>

              <!-- reset password -->
              <VCol cols="12">
                <VBtn
                  :loading="isOtpInserted"
                  :disabled="isOtpInserted"
                  block
                  type="submit"
                >
                  Verify my account
                </VBtn>
              </VCol>

              <!-- back to login -->
              <VCol cols="12">
                <div class="d-flex justify-center align-center flex-wrap">
                  <span class="me-1">Didn't get the code?</span>
                  <a href="#">Resend</a>
                </div>
              </VCol>
            </VRow>
          </VForm>
        </VCardText>
      </VCard>
    </div>
  </div>
</template>

